"""검색 쿼리 동의어 확장 (api-spec.md 섹션 3: 검색 로직).

문제 (2026-06-16 데모 중 발견):
  "미국 주식 하려면 어떻게 해야해?" 로 물으면 정답 문서
  "(1) 해외주식 매매 개요 및 제도"가 검색 상위 5건에 들지 못해
  LLM이 "편람에 포함되어 있지 않습니다"로 답함.
  - 벡터 임베딩이 '미국 ⊂ 해외주식' 관계를 충분히 못 잡음
  - BM25 bigram도 "미국/주식" 토큰이 "해외주식" 청크에 약하게만 걸림
  ("해외주식 하는법"은 정상 — 같은 의미인데 표현 차이로 갈림)

해결:
  검색 직전 질문에 도메인 동의어를 '덧붙여' 임베딩·BM25 양쪽 매칭을 보강.
  - LLM에 전달되는 사용자 질문 원문은 건드리지 않음 → 표시·출처 인용 영향 없음
    (rag.py: 확장 쿼리는 self.llm.embed / keyword_index.search 에만 사용)
  - 순수 파이썬 사전 → 무료·오프라인(폐쇄망 실도입), 결정적(데모 재현성).
    LLM 쿼리 재작성은 '첫 토큰 1초' 목표를 깨고 비결정적이라 PoC에서 제외.
  - '추가'만 하므로(원문 토큰을 지우지 않음) 기존 정상 검색을 해치지 않음.

이 모듈을 사용하는 곳:
  - backend/services/rag.py: RAGService.search() 진입부에서 expand_query() 호출
"""

# 트리거 표현 → 검색 쿼리에 덧붙일 동의어 목록.
#   - 좁은 표현(미국주식)은 넓은 정규 표현(해외주식)을 추가하는 '상위어 보강'이 핵심.
#   - 표기 흔들림(띄어쓰기)도 같은 방식으로 흡수.
#   - 키는 부분 문자열로 매칭하므로 조사가 붙어도 동작("미국 주식을" 포함).
# 확장 시 주의: 여기 넣는 표현이 많아질수록 BM25/임베딩에 노이즈가 섞일 수 있으니
#   '명백한 동의어/상위어'만 추가하고, 정확도 테스트(test_questions.json)로 검증할 것.
QUERY_EXPANSIONS: dict[str, list[str]] = {
    # 국가별 주식 질문 → 정규 표현 "해외주식" 문서로 연결 (데모 버그 직접 수정)
    "미국주식": ["해외주식"],
    "미국 주식": ["해외주식"],
    "미장": ["해외주식"],
    "미주식": ["해외주식"],
    "일본주식": ["해외주식"],
    "일본 주식": ["해외주식"],
    "중국주식": ["해외주식"],
    "중국 주식": ["해외주식"],
    "홍콩주식": ["해외주식"],
    "홍콩 주식": ["해외주식"],
    "해외증시": ["해외주식"],
    "해외 주식": ["해외주식"],
    # 계좌 개설 표기 흔들림(띄어쓰기) 상호 보강
    "계좌개설": ["계좌 개설"],
    "계좌 개설": ["계좌개설"],
    # 세금 약어
    "양도세": ["양도소득세"],
}


def expand_query(query: str) -> str:
    """질문에 도메인 동의어를 덧붙여 retrieval 매칭을 보강한다.

    QUERY_EXPANSIONS의 트리거 표현이 질문에 부분 문자열로 등장하면
    매핑된 동의어를 질문 뒤에 덧붙인다. 이미 질문에 있는 표현이나
    중복은 추가하지 않으며, 원문은 변형하지 않는다(추가만).

    예) "미국 주식 하려면 어떻게 해야해?"
        → "미국 주식 하려면 어떻게 해야해? 해외주식"
    """
    if not query:
        return query

    additions: list[str] = []
    for trigger, synonyms in QUERY_EXPANSIONS.items():
        if trigger in query:
            for term in synonyms:
                if term not in query and term not in additions:
                    additions.append(term)

    if not additions:
        return query
    return f"{query} {' '.join(additions)}"
